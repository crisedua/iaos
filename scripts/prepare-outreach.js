#!/usr/bin/env node
/**
 * prepare-outreach.js
 *
 * Reads leads-all.csv, scores each lead by ICP criteria, selects the top
 * N uncontacted leads for today, and generates a personalized message for
 * the chosen outreach channel.
 *
 * Outputs:
 *   outputs/leads/daily-outreach/YYYY-MM-DD-outreach.json  — machine-readable queue
 *   outputs/leads/daily-outreach/YYYY-MM-DD-outreach.md    — human-readable review file
 *
 * Updates:
 *   outputs/leads/outreach-tracker.json                     — marks selected leads as "queued"
 *
 * Usage:
 *   node scripts/prepare-outreach.js
 *   node scripts/prepare-outreach.js --channel sms          (default: whatsapp)
 *   node scripts/prepare-outreach.js --channel email
 *   node scripts/prepare-outreach.js --batch 10
 *   node scripts/prepare-outreach.js --batch 5 --dry-run
 *   node scripts/prepare-outreach.js --csv outputs/leads/gmap-extracts/2026-02-25/leads-all.csv
 *   node scripts/prepare-outreach.js --date 2026-02-25   (override output date)
 */

const fs   = require('fs');
const path = require('path');

// ── CLI args ─────────────────────────────────────────────────────────────────
const args = process.argv.slice(2);
const getArg = (flag, defaultVal) => {
  const i = args.indexOf(flag);
  return i !== -1 && args[i + 1] ? args[i + 1] : defaultVal;
};
const isDryRun   = args.includes('--dry-run');
const BATCH_SIZE = parseInt(getArg('--batch', '20'), 10);
const TODAY      = getArg('--date', new Date().toISOString().slice(0, 10));
const CSV_PATH   = getArg('--csv', `C:/ClaudeCode/outputs/leads/gmap-extracts/2026-02-25/leads-all.csv`);
const CHANNEL    = getArg('--channel', 'whatsapp');

const VALID_CHANNELS = ['whatsapp', 'sms', 'email'];
if (!VALID_CHANNELS.includes(CHANNEL)) {
  console.error(`ERROR: Invalid channel "${CHANNEL}". Must be one of: ${VALID_CHANNELS.join(', ')}`);
  process.exit(1);
}

const BASE         = 'C:/ClaudeCode/outputs/leads';
const TRACKER_PATH = path.join(BASE, 'outreach-tracker.json');
const OUT_DIR      = path.join(BASE, 'daily-outreach');

// ── Category label mapping ────────────────────────────────────────────────────
const CATEGORY_LABELS = {
  dentistas:    'clínicas dentales',
  abogados:     'estudios jurídicos',
  clinicas:     'clínicas médicas',
  inmobiliarias: 'agencias inmobiliarias',
};

// ── ICP scoring ───────────────────────────────────────────────────────────────
// Max score = 5
function icpScore(lead) {
  let score = 0;
  const reviewCount = parseInt(lead.review_count, 10) || 0;
  if (reviewCount >= 20)  score += 2;
  if (lead.phone)         score += 1;
  if (lead.website)       score += 1;
  if (lead.category === 'dentistas' || lead.category === 'clinicas') score += 1;
  return score;
}

// ── Message templates ─────────────────────────────────────────────────────────
function buildMessage(lead, channel) {
  const label = CATEGORY_LABELS[lead.category] || 'negocios locales';
  if (channel === 'sms') {
    // SMS: ≤160 chars, punchy, includes callback CTA
    return (
      `Hola ${lead.business_name}, soy Esteban. ` +
      `Instalo agentes de voz IA para ${label} en Chile — contestan 24/7. ` +
      `¿Pierden llamadas fuera de horario? Responda aquí para más info.`
    );
  }
  if (channel === 'email') {
    // Email: subject + body are generated by send-email.js; message field stores body preview
    return (
      `Hola equipo de ${lead.business_name},\n\n` +
      `Mi nombre es Esteban y trabajo con ${label} en Chile instalando agentes de voz con IA que contestan llamadas las 24 horas.\n\n` +
      `Vi su negocio en Google Maps y quería preguntar: ¿suelen perder llamadas cuando están ocupados o fuera de horario?\n\n` +
      `Si les interesa ver cómo funciona, puedo mostrarles en 15 minutos. ¿Tienen disponibilidad esta semana?\n\n` +
      `Saludos,\nEsteban\nAI OS for Business`
    );
  }
  // Default: whatsapp
  return (
    `Hola ${lead.business_name}, le escribo porque vi su negocio en Google Maps. ` +
    `Me llamo Esteban y trabajo con ${label} en Chile instalando un agente de voz con IA que contesta llamadas 24/7.\n` +
    `Vi que reciben bastantes consultas y quería preguntar: ¿suelen perder llamadas cuando están ocupados o fuera de horario?\n` +
    `Si les interesa, con gusto les muestro cómo funciona en 15 minutos. ¿Tienen disponibilidad esta semana?`
  );
}

// ── CSV parser (no external deps) ────────────────────────────────────────────
function parseCSV(content) {
  const lines = content.split(/\r?\n/).filter(Boolean);
  if (lines.length < 2) return [];
  const headers = lines[0].split(',').map(h => h.trim());

  return lines.slice(1).map(line => {
    const cols = [];
    let current = '';
    let inQuote = false;
    for (let i = 0; i < line.length; i++) {
      const ch = line[i];
      if (ch === '"') {
        if (inQuote && line[i + 1] === '"') { current += '"'; i++; }
        else inQuote = !inQuote;
      } else if (ch === ',' && !inQuote) {
        cols.push(current.trim());
        current = '';
      } else {
        current += ch;
      }
    }
    cols.push(current.trim());

    const obj = {};
    headers.forEach((h, idx) => { obj[h] = cols[idx] || ''; });
    return obj;
  });
}

// ── Main ──────────────────────────────────────────────────────────────────────
console.log(`\n── prepare-outreach.js ──────────────────────────`);
console.log(`Date:       ${TODAY}`);
console.log(`Channel:    ${CHANNEL}`);
console.log(`Batch size: ${BATCH_SIZE}`);
console.log(`Dry run:    ${isDryRun}`);
console.log(`CSV:        ${CSV_PATH}\n`);

// 1. Load leads
if (!fs.existsSync(CSV_PATH)) {
  console.error(`ERROR: CSV not found at ${CSV_PATH}`);
  console.error(`Run /extract-leads-gmap first, then update --csv path if needed.`);
  process.exit(1);
}
const leads = parseCSV(fs.readFileSync(CSV_PATH, 'utf8'));
console.log(`Loaded ${leads.length} leads from CSV.`);

// 2. Load tracker — build set of already-contacted keys
let tracker = { meta: { created: TODAY, total_contacted: 0, total_sent: 0, total_responded: 0 }, contacts: {} };
if (fs.existsSync(TRACKER_PATH)) {
  try {
    tracker = JSON.parse(fs.readFileSync(TRACKER_PATH, 'utf8'));
  } catch (e) {
    console.warn('Warning: could not parse outreach-tracker.json, starting fresh.');
  }
}
const contactedKeys = new Set(Object.keys(tracker.contacts));
const activelyContacted = Object.values(tracker.contacts).filter(e => e.status && e.status !== 'uncontacted').length;
console.log(`Already contacted: ${activelyContacted} leads (${contactedKeys.size} total tracker entries).`);

// 3. Score and filter — apply channel-specific contact field requirement
const uncontacted = leads
  .filter(lead => {
    const key   = lead.phone || lead.maps_url;
    if (!key) return false;
    const entry  = tracker.contacts[key];
    // Skip leads that have been actively contacted (queued or beyond)
    // Allow 'uncontacted' status entries — those were added by find-emails.js for enrichment only
    if (entry && entry.status && entry.status !== 'uncontacted') return false;
    // Channel-specific filter
    if (CHANNEL === 'whatsapp' || CHANNEL === 'sms') return !!lead.phone;
    if (CHANNEL === 'email') {
      // Require email to be present in tracker entry
      return !!(entry && entry.email);
    }
    return true;
  })
  .map(lead => ({ ...lead, icp_score: icpScore(lead) }))
  .sort((a, b) => b.icp_score - a.icp_score || (parseInt(b.review_count,10)||0) - (parseInt(a.review_count,10)||0));

console.log(`Uncontacted leads eligible: ${uncontacted.length}`);

if (uncontacted.length === 0) {
  console.log('\nAll leads have already been contacted. Run /extract-leads-gmap to get fresh leads.');
  process.exit(0);
}

// 4. Select batch
const batch = uncontacted.slice(0, BATCH_SIZE);
const highScore = batch[0].icp_score;
const lowScore  = batch[batch.length - 1].icp_score;
console.log(`\nSelected ${batch.length} leads. ICP score range: ${lowScore}–${highScore}\n`);

// 5. Build queue with messages (channel-aware)
const queue = batch.map(lead => {
  const key     = lead.phone || lead.maps_url;
  const entry   = tracker.contacts[key] || {};
  return {
    phone:         lead.phone,
    email:         entry.email || null,
    business_name: lead.business_name,
    city:          lead.city,
    category:      lead.category,
    maps_url:      lead.maps_url,
    website:       lead.website,
    review_count:  parseInt(lead.review_count, 10) || 0,
    rating:        lead.rating,
    icp_score:     lead.icp_score,
    channel:       CHANNEL,
    message:       buildMessage(lead, CHANNEL),
  };
});

// 6. Ensure output directory exists
if (!isDryRun) {
  fs.mkdirSync(OUT_DIR, { recursive: true });
}

// 7. Write JSON queue
const jsonPath = path.join(OUT_DIR, `${TODAY}-outreach.json`);
const mdPath   = path.join(OUT_DIR, `${TODAY}-outreach.md`);

if (!isDryRun) {
  fs.writeFileSync(jsonPath, JSON.stringify(queue, null, 2), 'utf8');
  console.log(`Queue JSON: ${jsonPath}`);
}

// 8. Write human-readable MD
const SEND_COMMAND = {
  whatsapp: 'node scripts/send-whatsapp.js',
  sms:      'node scripts/send-sms.js',
  email:    'node scripts/send-email.js',
}[CHANNEL] || 'node scripts/send-whatsapp.js';

const mdLines = [
  `# Daily Outreach — ${TODAY}`,
  ``,
  `**Channel:** ${CHANNEL} | **Batch size:** ${batch.length} | **ICP score range:** ${lowScore}–${highScore}`,
  `**CSV source:** ${CSV_PATH}`,
  ``,
  `> Review these messages before sending. Run \`${SEND_COMMAND}\` to send.`,
  ``,
  `---`,
  ``,
];

queue.forEach((lead, i) => {
  mdLines.push(`## ${i + 1}. ${lead.business_name}`);
  mdLines.push(``);
  mdLines.push(`| Field | Value |`);
  mdLines.push(`|---|---|`);
  mdLines.push(`| Category | ${lead.category} |`);
  mdLines.push(`| City | ${lead.city} |`);
  mdLines.push(`| Phone | ${lead.phone || '_(none)_'} |`);
  mdLines.push(`| Website | ${lead.website || '_(none)_'} |`);
  mdLines.push(`| Reviews | ${lead.review_count} (${lead.rating}⭐) |`);
  mdLines.push(`| ICP Score | ${lead.icp_score}/5 |`);
  mdLines.push(`| Maps | ${lead.maps_url} |`);
  mdLines.push(``);
  mdLines.push(`**WhatsApp message:**`);
  mdLines.push(``);
  mdLines.push(`> ${lead.message.replace(/\n/g, '\n> ')}`);
  mdLines.push(``);
  mdLines.push(`---`);
  mdLines.push(``);
});

if (!isDryRun) {
  fs.writeFileSync(mdPath, mdLines.join('\n'), 'utf8');
  console.log(`Review MD:  ${mdPath}`);
}

// 9. Update tracker — mark as queued
if (!isDryRun) {
  queue.forEach(lead => {
    const key = lead.phone || lead.maps_url;
    if (key) {
      const existing = tracker.contacts[key] || {};
      tracker.contacts[key] = {
        ...existing,
        business_name:   lead.business_name,
        category:        lead.category,
        city:            lead.city,
        phone:           lead.phone,
        maps_url:        lead.maps_url,
        status:          'queued',
        channel:         CHANNEL,
        first_contacted: existing.first_contacted || null,
        last_contact:    existing.last_contact    || null,
        message_sent:    lead.message,
        response_notes:  existing.response_notes  || '',
        queued_date:     TODAY,
      };
    }
  });
  tracker.meta.total_contacted = Object.keys(tracker.contacts).length;
  fs.writeFileSync(TRACKER_PATH, JSON.stringify(tracker, null, 2), 'utf8');
  console.log(`Tracker:    ${TRACKER_PATH} (${Object.keys(tracker.contacts).length} total entries)`);
}

// 10. Summary
console.log(`\n── Summary ──────────────────────────────────────`);
console.log(`Selected:    ${batch.length} leads queued for ${TODAY}`);
console.log(`High score:  ${highScore}/5`);
console.log(`Low score:   ${lowScore}/5`);
console.log(`Remaining:   ${uncontacted.length - batch.length} uncontacted leads in CSV`);
if (isDryRun) {
  console.log(`\n[DRY RUN] No files written. Remove --dry-run to execute.`);
  console.log(`\nSample message for lead #1 (${queue[0].business_name}):`);
  console.log(`─────────────────────────────────────────────────`);
  console.log(queue[0].message);
  console.log(`─────────────────────────────────────────────────`);
} else {
  console.log(`\nNext step: review ${TODAY}-outreach.md then run:`);
  console.log(`  ${SEND_COMMAND}`);
}
